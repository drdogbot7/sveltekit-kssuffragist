			<h3 class="!my-0">
				Helen E. Starrett
				<span class="font-sans text-sm font-normal text-gray-600">(1840-1920)</span>
			</h3>
			<div class="text-sm uppercase">Lawrence</div>
			<p>
				After meeting Susan B. Anthony, Starrett became active in the 1867 suffrage campaign with
				her husband. As a prolific author, lecturer, and journalist, she wrote about her experiences
				in <i>History of Woman Suffrage (Vol. 2)</i> and attended the first (1869) and last (1920) national
