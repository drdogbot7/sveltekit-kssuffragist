			1912, after Lizzie S. Sheldon wrote this resolution: “The right of citizens of the state of
			Kansas to vote and hold offices shall not be denied or abridged on account of sex.” In 1919,
			Jane L. Brooks founded the first state chapter of the League of Women Voters in Kansas, before
			the Nineteenth Amendment of the US Constitution granted voting rights to women in 1920.
		</p>
