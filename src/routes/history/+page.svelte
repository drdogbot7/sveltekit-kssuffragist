			Twenty years later, Kansas became the first state to grant municipal suffrage to women on
			February 15, 1887, Susan B. Anthony&#8216;s birthday. Despite a vigorous campaign in 1894,
			male voters rejected a woman suffrage amendment. Yet Kansas women won universal suffrage in
			1912, after Lizzie S. Sheldon wrote this resolution: “The right of citizens of the state of
			Kansas to vote and hold offices shall not be denied or abridged on account of sex.” In 1919,
			Jane M. Brooks founded the first state chapter of the League of Women Voters, before the
			Nineteenth Amendment of the US Constitution granted voting rights to women in 1920.
		</p>
