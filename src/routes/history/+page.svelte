			Suffragism has a long and storied history in the state of Kansas. In the wake of the Wyandotte
			Constitutional Convention, which established Kansas as a free state in 1859, Kansas
			suffragists waged five campaigns to win women&#8216;s right to vote. After statehood in 1861,
